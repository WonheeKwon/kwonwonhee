package day01;

public class Oper2 {
	public static void main(String[] args) {
		String 글자1 = "Hello";
		String 글자2 ="JAVA";
		String 글자 = 글자1+글자2;
		
		
		System.out.println(글자);
		System.out.println("일\t이");         //enter 위에 짝대기
		
		// \t 는 띄어쓰기
		// \n 는 한줄띄우기
		System.out.println("안녕\n하세요");
		
		
		//" 사용히려면
		System.out.println("누구는 말했다.\"반가워\".");
		
	}
}
